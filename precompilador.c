/*
La directivas de pre procesamiento.

El pre procesador proporciona un conjunto de directivas que resultan una herreamient sumamente útil al programador
Todas la directivas comienzan siempre por el simbolo #.

Algunas de las mas importantes:
    #include: inclusion de ficheros
    #define: creacion de constantes simbolicas.
    #undef: eliminacion de constantes simbolicas.
    #if(#else, #enif): inclusion condicional del codigo.

printf:

%d: numero entero
%f: numero real (flotante).
%c: caracter.
%s: string.

<stdio.h>: standard input-output header
    archivo de cabecera que contiene las definiciones de las amcros, las constantes, las declaraciones
    de funcioones de la biblioteca estandar del lenguaje de programacion C, para hacer operaciones,
    estandar, de entrasa y salida, asi como la definicon de tipos necesarias para dichas operaciones.
    


Algoritmo: Serie de passo que deben ejecutarse para un determinado fin.
Tipo de dato: Espacios de memoria reservados con un tamaño especifico.
Struct: Tipos de datos creados por el usuario. Ej: Datos realcionados con una persona (nombre, edad, sexo, etc).
Función: Pedazo de codigo que tiene asociado un nombre y retorna un valor al finalizar su tarea.
Const: Permite que el valor de una variable no pueda ser sobreescrito.
*/

